// import logo from './logo.svg';
import './App.css';
import Box from './box.js'



class App extends Component {

  constructor(props) {
     super(props);
     const Boxes = Array(32).fill().map(this.getRandomColor,this)
     this.state = {Boxes}


     setInterval(() => {
        const Boxes = this.state.Boxes.slice()
        const randBoxIndex = Math.floor(Math.random() * 32) + 1
        Boxes[randBoxIndex] = this.getRandomColor()
        // console.log(randBoxIndex,Boxes[randBoxIndex])
        this.setState({Boxes})
      },300)
   };

   getRandomColor(){
       const randColorIndex = Math.floor(Math.random() * this.props.allColors.length) + 1
       return this.props.allColors[randColorIndex]
   }



  render() {

    const Boxes = this.state.Boxes.map((color, index) => (
      <Box key={index} backgroundcolor={color} />
    ))
    return (
      <div className="App">
        {Boxes}
      </div>

    );
  }
}

